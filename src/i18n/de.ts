// Complete in t('…')). Englisch wird aber gegen einen string-weichen
// Spiegel-Typ geprüft, sonst meckert TS bei jedem Übersetzungs-Wert.
type WidenStrings<T> = {
  [K in keyof T]: T[K] extends string ? string : WidenStrings<T[K]>;
};

